package it.coluccia.codechallenge2018.boh;

public class Assegnamento {

	private int indiceProvider;
	private int indiceRegionProvider;
	private int numPacchetti;
	public int getIndiceProvider() {
		return indiceProvider;
	}
	public void setIndiceProvider(int indiceProvider) {
		this.indiceProvider = indiceProvider;
	}
	public int getIndiceRegionProvider() {
		return indiceRegionProvider;
	}
	public void setIndiceRegionProvider(int indiceRegionProvider) {
		this.indiceRegionProvider = indiceRegionProvider;
	}
	public int getNumPacchetti() {
		return numPacchetti;
	}
	public void setNumPacchetti(int numPacchetti) {
		this.numPacchetti = numPacchetti;
	}
	
	
}
